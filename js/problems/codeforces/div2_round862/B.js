var B = function (string) {
    let char = string[0]
    let pos = 0

    for (let i = 1; i < string.length; i++) {
        if (string[i] <= char) {
            char = string[i]
            pos = i
        }
    }

    if (pos === 0) return string

    return string[pos] + string.substring(0, pos) + string.substring(pos + 1)
};

